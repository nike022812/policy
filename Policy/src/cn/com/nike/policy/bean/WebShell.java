package cn.com.nike.policy.bean;

/**
 * webshell bean
 * @author nike
 * @version 1.0
 */
public class WebShell {
	
	/**
	 * shell地址
	 */
	private String url;
	
	/**
	 * shell密码
	 */
	private String pass;
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
}
